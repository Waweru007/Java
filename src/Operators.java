public class Operators {
    public static void main(String[] args) {
        int x=5;
        int y=7;
        int z=8;
        int h=9;
        --z;
        ++h;
        System.out.println(x+y );
        System.out.println(x-y );
        System.out.println(x*y );
        System.out.println(y%x );
        System.out.println(z);
        System.out.println(h);

    }
}